const $ = require('jquery')
let language = -1

const translateElement = (id) => {
  if(language === -1) {
    return
  }
  let text = $(`#${id}`).html()
  let out = t[text]
  if(out) {
    $(`#${id}`).html(out)
  }
}

const translateText = (text) => {
  if(language === -1) {
    return text
  }
  let out = t[text]
  if(out) {
    return out
  }
  return text
}

const translateSub = (text) => {
  if(language === -1) {
    return text
  }
  let out = text
  subs.forEach(sub => {
    out = out.replace(sub[0], sub[1])
  })
  if(out) {
    return out
  }
  return text
}

const translate = () => {
  if(language === -1) {
    return
  }
  ids.forEach(id => translateElement(id, $(`#${id}`).html()))
  for(let i = 1; i < 26; i++) {
    let id = `tr-${i}`
    translateElement(id, $(`#${id}`).html())
  }
  $('h2').each(function() {
    if($(this).html()) {
      $(this).html(translateText($(this).html()))
    }
  })
  $('option').each(function() {
    if($(this).html()) {
      $(this).html(translateText($(this).html()))
    }
  })
  $('li').each(function() {
    if($(this).html()) {
      $(this).html(translateText($(this).html()))
    }
  })
  $('span').each(function() {
    if($(this).html()) {
      $(this).html(translateText($(this).html()))
    }
  })
}

const setLanguage = (lg) => {
  language = lg
  translate()
}

module.exports = {
  setLanguage,
  translate,
  translateElement,
  translateText,
  translateSub
}
